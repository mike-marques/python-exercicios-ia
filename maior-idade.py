# 4️⃣ Verificando Maioridade
# Recebe a idade do usuário e verifica se ele é maior de idade (18 anos ou mais).
# Ensina estruturas condicionais e lógica booleana.

idade = int(input("Digite sua idade: "))
if idade >= 18:
    print("Você é maior de idade.")
else:
    print("Você é menor de idade.")